/** 从正文末尾提取情绪强度标记，返回 { cleanedContent, intensity } */
export function extractEmotionIntensity(content: string): { cleanedContent: string; intensity: number | null } {
  const match = content.match(/<!--\s*emotion_intensity:\s*(\d{1,2})\s*-->\s*$/m)
  if (!match) return { cleanedContent: content, intensity: null }
  const intensity = parseInt(match[1], 10)
  if (intensity < 1 || intensity > 10) return { cleanedContent: content, intensity: null }
  return {
    cleanedContent: content.replace(match[0], '').trimEnd(),
    intensity
  }
}
