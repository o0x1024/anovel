export type StoryMainCategory = typeof STORY_MAIN_CATEGORIES[number]['value']

export interface StoryCategoryTags {
  main_category: string
  plot: string[]
  character: string[]
  emotion: string[]
  setting: string[]
}

export const EMPTY_STORY_CATEGORY_TAGS: StoryCategoryTags = {
  main_category: '',
  plot: [],
  character: [],
  emotion: [],
  setting: []
}

const MAIN_SET = new Set<string>(STORY_MAIN_CATEGORIES.map(x => x.value))
const PLOT_SET = new Set<string>(STORY_PLOT_TAGS)
const CHARACTER_SET = new Set<string>(STORY_CHARACTER_TAGS)
const EMOTION_SET = new Set<string>(STORY_EMOTION_TAGS)
const SETTING_SET = new Set<string>(STORY_SETTING_TAGS)

function cleanList(input: unknown, allowed: Set<string>): string[] {
  if (!Array.isArray(input)) return []
  return [...new Set(input.filter((x): x is string => typeof x === 'string').map(x => x.trim()).filter(x => allowed.has(x)))]
}

export function inferStoryMainCategory(text: string): StoryMainCategory | '' {
  if (!text || typeof text !== 'string') return ''
  const normalized = text.toLowerCase()
  let best: { value: StoryMainCategory; score: number } | null = null
  for (const cat of STORY_MAIN_CATEGORIES) {
    let score = 0
    if (normalized.includes(cat.value.toLowerCase())) score += 10
    const tokens = cat.desc.split(/[，,、；;。]+/).map(t => t.trim()).filter(Boolean)
    for (const token of tokens) {
      if (token.length >= 2 && normalized.includes(token.toLowerCase())) score += 2
    }
    if (score > 0 && (!best || score > best.score)) best = { value: cat.value, score }
  }
  return best?.value ?? ''
}

export function normalizeStoryCategoryTags(input: unknown, fallbackText?: string): StoryCategoryTags {
  if (!input || typeof input !== 'object') return { ...EMPTY_STORY_CATEGORY_TAGS }
  const row = input as Record<string, unknown>
  let main = typeof row.main_category === 'string' && MAIN_SET.has(row.main_category.trim()) ? row.main_category.trim() : ''
  if (!main && fallbackText) {
    const inferred = inferStoryMainCategory(fallbackText)
    if (inferred) main = inferred
  }
  return {
    main_category: main,
    plot: cleanList(row.plot, PLOT_SET),
    character: cleanList(row.character, CHARACTER_SET),
    emotion: cleanList(row.emotion, EMOTION_SET),
    setting: cleanList(row.setting, SETTING_SET)
  }
}

export function flattenStoryCategoryTags(tags: StoryCategoryTags): string[] {
  return [tags.main_category, ...tags.plot, ...tags.character, ...tags.emotion, ...tags.setting].filter(Boolean)
}

export function storyCategoryTagsToStorage(tags: StoryCategoryTags): string {
  return JSON.stringify(flattenStoryCategoryTags(normalizeStoryCategoryTags(tags)))
}

export function tagsArrayToStoryCategoryTags(input: string[]): StoryCategoryTags {
  return normalizeStoryCategoryTags({
    main_category: input.find(x => MAIN_SET.has(x)) ?? '',
    plot: input,
    character: input,
    emotion: input,
    setting: input
  })
}

export function parseStoryCategoryTagsFromWork(genre?: string | null, tagsText?: string | null): StoryCategoryTags {
  let rawTags: string[] = []
  if (tagsText) {
    try {
      const parsed = JSON.parse(tagsText)
      if (Array.isArray(parsed)) rawTags = parsed.filter((x): x is string => typeof x === 'string')
    } catch {
      rawTags = []
    }
  }
  const mainFromTags = !genre ? rawTags.find(x => MAIN_SET.has(x)) ?? '' : ''
  return normalizeStoryCategoryTags({
    main_category: genre ?? mainFromTags,
    plot: rawTags,
    character: rawTags,
    emotion: rawTags,
    setting: rawTags
  })
}

